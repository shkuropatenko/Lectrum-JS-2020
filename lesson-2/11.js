// У вас есть массив с элементами в виде положительных чисел.
// Найдите сумму таких элементов массива.
// Использовать встроенные методы массивов — нельзя.

////////////////// Задание //////////////////
// const array = [2, -1, -3, 15, 0, 4];

////////////////// Решение //////////////////

const array = [2, -1, -3, 15, 0, 4];
let sum = null;
for (const value of array) {
  if (value > 0) {
    sum += value;
  }
}
console.log(sum);